% --- Executes on button press in lihat.
function lihat_Callback(hObject, eventdata, handles)
% hObject    handle to lihat (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% Pengambilan Data User
% Fuel
contents = cellstr(get(handles.fueltype,'String'));
fuel = contents(get(handles.fueltype,'Value'));
if(strcmp(fuel,"Electric"))
    fuel = 1;
elseif(strcmp(fuel,"Hybrid"))
    fuel = 2;
elseif(strcmp(fuel,"Gasoline"))
    fuel = 3;
else
    fuel = 4;
end

% Rating
rating = str2double(get(handles.rating,'string'));

% Trips Taken
trip = str2double(get(handles.tripstaken,'string'));

% Review
review = str2double(get(handles.review,'string'));

% Car Type
contents = cellstr(get(handles.cartype,'String'));
cartype = contents(get(handles.cartype,'Value'));
if(strcmp(cartype,"Car"))
    cartype = 1;
elseif(strcmp(cartype,"SUV"))
    cartype = 2;
else
    cartype = 3;
end

% Normalisasi Data & Pembuatan Array
fuel = 0.1 + 0.8*(fuel-1)/(4-1);
rating = 0.1 + 0.8*(rating-4.48)/(5-4.48);
trip = 0.1 + 0.8*(trip-1)/(223-1);
review = 0.1 + 0.8*(review-1)/(177-1);
cartype = 0.1 + 0.8*(cartype-1)/(3-1);
data_prediksi = [fuel rating trip review cartype]';

load net.mat;

% Prediksi
hasil_prediksi = sim(net_keluaran,data_prediksi);
max_data = 3;
min_data = 0;
hasil_prediksi = ((hasil_prediksi-0.1)*(max_data-min_data)/0.8)+min_data;

%lihat prediksi
set(handles.akurasi,'string',hasil_prediksi);
if(hasil_prediksi>=0 && hasil_prediksi<0.5)
        set(handles.hasil,'string','Tesla');
    elseif(hasil_prediksi>=0.5 && hasil_prediksi<1.5)
        set(handles.hasil,'string','Toyota');
    elseif(hasil_prediksi>=1.5 && hasil_prediksi<2.5)
        set(handles.hasil,'string','Ford');
    elseif(hasil_prediksi>=2.5 && hasil_prediksi<3.5)
        set(handles.hasil,'string','BMW');
end

